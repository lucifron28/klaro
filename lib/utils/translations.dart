  /// Get translation for a given text and language code
  static String translate(String text, String languageCode) {
    // Default to English if language not found
    if (!translations.containsKey(languageCode)) {
      return text;
    }

    // Return translation or original text if not found
    return translations[languageCode]![text] ?? text;
  }

  /// Get all supported language codes
  static List<String> getSupportedLanguages() {
    return translations.keys.toList();
  }
